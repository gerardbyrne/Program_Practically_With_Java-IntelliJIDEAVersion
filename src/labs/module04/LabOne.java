package labs.module04;
/*
 Write a Java console application that will calculate and display the area of a rectangle using
  the length and breadth that are input at the console by the user. 
*/
public class LabOne 
{
public static void main(String[] args) 
{		
        int length;
        int breadth;
        int area;
        
        length = 20;
        breadth = 10;
        
        area = length * breadth;

        System.out.println("");
        System.out.println("The area of the rectangle is " + area + " square centimetres");
        System.out.println("");
        
     } // This is the end of the method    
} // This is the end of the class